public class JsonConverter
{
	private readonly SerializationContext context;

	private readonly FormatReader reader;

	private readonly StringBuilder builder;

	private int indentationSize;

	public static string Encode(Stream stream, SerializationContext context = null)
	{
		JsonConverter jsonConverter = new JsonConverter(stream, context);
		try
		{
			return jsonConverter.AppendStream().ToString();
		}
		catch (Exception ex)
		{
			ex.Source = jsonConverter.builder.ToString();
			throw;
		}
	}

	private JsonConverter(Stream stream, SerializationContext context = null)
	{
		this.context = context ?? SerializationContext.Default;
		reader = new FormatReader(stream);
		builder = new StringBuilder();
		indentationSize = 0;
	}

	private JsonConverter AppendStream()
	{
		Format format = reader.ReadFormat();
		if (format.IsNil)
		{
			Append("null");
		}
		else if (format.IsFalse)
		{
			Append("false");
		}
		else if (format.IsTrue)
		{
			Append("true");
		}
		else if (format.IsPositiveFixInt)
		{
			Append(reader.ReadPositiveFixInt(format).ToString());
		}
		else if (format.IsUInt8)
		{
			Append(reader.ReadUInt8().ToString());
		}
		else if (format.IsUInt16)
		{
			Append(reader.ReadUInt16().ToString());
		}
		else if (format.IsUInt32)
		{
			Append(reader.ReadUInt32().ToString());
		}
		else if (format.IsUInt64)
		{
			Append(reader.ReadUInt64().ToString());
		}
		else if (format.IsNegativeFixInt)
		{
			Append(reader.ReadNegativeFixInt(format).ToString());
		}
		else if (format.IsInt8)
		{
			Append(reader.ReadInt8().ToString());
		}
		else if (format.IsInt16)
		{
			Append(reader.ReadInt16().ToString());
		}
		else if (format.IsInt32)
		{
			Append(reader.ReadInt32().ToString());
		}
		else if (format.IsInt64)
		{
			Append(reader.ReadInt64().ToString());
		}
		else if (format.IsFloat32)
		{
			Append(reader.ReadFloat32().ToString());
		}
		else if (format.IsFloat64)
		{
			Append(reader.ReadFloat64().ToString());
		}
		else if (format.IsFixStr)
		{
			AppendQuotedString(reader.ReadFixStr(format));
		}
		else if (format.IsStr8)
		{
			AppendQuotedString(reader.ReadStr8());
		}
		else if (format.IsStr16)
		{
			AppendQuotedString(reader.ReadStr16());
		}
		else if (format.IsStr32)
		{
			AppendQuotedString(reader.ReadStr32());
		}
		else if (format.IsBin8)
		{
			StringifyBinary(reader.ReadBin8());
		}
		else if (format.IsBin16)
		{
			StringifyBinary(reader.ReadBin16());
		}
		else if (format.IsBin32)
		{
			StringifyBinary(reader.ReadBin32());
		}
		else if (format.IsArrayFamily)
		{
			ReadArray(format);
		}
		else if (format.IsMapFamily)
		{
			ReadMap(format);
		}
		else
		{
			if (!format.IsExtFamily)
			{
				throw new FormatException(format, reader);
			}
			ReadExt(format);
		}
		return this;
	}

	public override string ToString()
	{
		return builder.ToString();
	}

	private JsonConverter Indent()
	{
		if (context.JsonOptions.PrettyPrint)
		{
			for (int i = 0; i < indentationSize; i++)
			{
				Append(context.JsonOptions.IndentationString);
			}
		}
		return this;
	}

	private JsonConverter Append(string str)
	{
		builder.Append(str);
		return this;
	}

	private JsonConverter AppendIfPretty(string str)
	{
		if (context.JsonOptions.PrettyPrint)
		{
			Append(str);
		}
		return this;
	}

	private JsonConverter ValueSeparator()
	{
		AppendIfPretty(context.JsonOptions.ValueSeparator);
		return this;
	}

	private JsonConverter AppendQuotedString(string str)
	{
		return Append("\"").Append(str).Append("\"");
	}

	private void StringifyBinary(byte[] bytes)
	{
		Append("[");
		foreach (byte b in bytes)
		{
			Append("0x").Append(b.ToString("X2")).Append(",");
		}
		Append("]");
	}

	private void ReadArray(Format format)
	{
		int num = reader.ReadArrayLength(format);
		if (num == 0)
		{
			Append("[]");
			return;
		}
		Append("[").ValueSeparator();
		indentationSize++;
		for (int i = 0; i < num; i++)
		{
			Indent().AppendStream();
			if (i < num - 1)
			{
				Append(",");
			}
			ValueSeparator();
		}
		indentationSize--;
		Indent().Append("]");
	}

	private void ReadMap(Format format)
	{
		int num = reader.ReadMapLength(format);
		if (num == 0)
		{
			Append("{}");
			return;
		}
		Append("{").ValueSeparator();
		indentationSize++;
		for (int i = 0; i < num; i++)
		{
			Indent().AppendStream().Append(":").AppendIfPretty(" ")
				.AppendStream();
			if (i < num - 1)
			{
				Append(",");
			}
			ValueSeparator();
		}
		indentationSize--;
		Indent().Append("}");
	}

	private void ReadExt(Format format)
	{
		uint length = reader.ReadExtLength(format);
		sbyte extType = reader.ReadExtType(format);
		object obj = context.TypeHandlers.GetExt(extType).ReadExt(length, reader);
		Append(obj.ToString());
	}
}
public class MapDefinition
{
	private const BindingFlags MethodFlags = BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.InvokeMethod;

	private static readonly Type[] serializableUnityTypes = new Type[8]
	{
		typeof(Color),
		typeof(Color32),
		typeof(Vector2),
		typeof(Vector3),
		typeof(Vector4),
		typeof(Quaternion),
		typeof(Vector2Int),
		typeof(Vector3Int)
	};

	private static readonly Type[] callbackTypes = new Type[4]
	{
		typeof(OnDeserializingAttribute),
		typeof(OnDeserializedAttribute),
		typeof(OnSerializingAttribute),
		typeof(OnSerializedAttribute)
	};

	public readonly Type Type;

	public readonly Dictionary<string, FieldInfo> FieldInfos;

	public readonly Dictionary<string, ITypeHandler> FieldHandlers;

	public readonly Dictionary<Type, MethodInfo[]> Callbacks;

	internal MapDefinition(SerializationContext context, Type type)
	{
		Type = type;
		if (!IsSerializable(context, type))
		{
			throw new CustomAttributeFormatException(type?.ToString() + " does not have System.SerializableAttribute defined");
		}
		FieldInfos = new Dictionary<string, FieldInfo>();
		FieldInfo[] fields = type.GetFields(context.MapOptions.FieldFlags);
		foreach (FieldInfo fieldInfo in fields)
		{
			if (IsFieldSerializable(context, fieldInfo))
			{
				FieldInfos[fieldInfo.Name] = fieldInfo;
			}
		}
		FieldHandlers = new Dictionary<string, ITypeHandler>();
		foreach (FieldInfo value in FieldInfos.Values)
		{
			FieldHandlers.Add(value.Name, context.TypeHandlers.Get(value.FieldType));
		}
		Callbacks = new Dictionary<Type, MethodInfo[]>();
		MethodInfo[] methods = type.GetMethods(BindingFlags.Instance | BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.InvokeMethod);
		Type[] array = callbackTypes;
		foreach (Type type2 in array)
		{
			List<MethodInfo> list = new List<MethodInfo>();
			MethodInfo[] array2 = methods;
			foreach (MethodInfo methodInfo in array2)
			{
				if (AttributesExist(methodInfo, type2))
				{
					list.Add(methodInfo);
				}
			}
			if (list.Count > 0)
			{
				Callbacks[type2] = list.ToArray();
			}
		}
	}

	private bool IsSerializable(SerializationContext context, Type type)
	{
		if (!context.MapOptions.RequireSerializableAttribute)
		{
			return true;
		}
		if (Array.IndexOf(serializableUnityTypes, type) != -1)
		{
			return true;
		}
		return type.IsSerializable;
	}

	private bool AttributesExist(MemberInfo info, Type attributeType)
	{
		return info.GetCustomAttributes(attributeType, inherit: true).Length != 0;
	}

	private bool IsFieldSerializable(SerializationContext context, FieldInfo info)
	{
		if (AttributesExist(info, typeof(NonSerializedAttribute)))
		{
			return false;
		}
		if (context.MapOptions.IgnoreAutoPropertyValues && info.Name.StartsWith("<"))
		{
			return false;
		}
		return IsSerializable(context, info.FieldType);
	}
