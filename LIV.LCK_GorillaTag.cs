		{
			await Task.Delay(2000);
			SetDisabled(isDisabled: false);
			if (_lckService != null)
			{
				if (_lckService.IsRecording().Result)
				{
					_currentState = State.Recording;
				}
				else
				{
					_currentState = State.Idle;
				}
			}
			else
			{
				_currentState = State.Idle;
			}
			UpdateVisualState();
		}

