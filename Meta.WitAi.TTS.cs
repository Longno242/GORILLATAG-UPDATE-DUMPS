		public virtual void LoadClips()
		{
			if (_clips != null)
			{
				VLog.W("Cannot autoload clips twice.");
				return;
			}
			_phrases = GetAllPhrases().ToArray();
			List<TTSClipData> list = new List<TTSClipData>();
			string[] phrases = _phrases;
			foreach (string textToSpeak in phrases)
