			{
				return "No TTS Service found on speaker";
			}
			string invalidError = base.Speaker.TTSService.GetInvalidError();
			if (!string.IsNullOrEmpty(invalidError))
			{
				return invalidError;
			}
			if (!string.IsNullOrEmpty(_lastLoadError))
			{
				return _lastLoadError;
			}
			return string.Empty;
