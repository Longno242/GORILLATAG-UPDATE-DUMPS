		private byte getNewVoiceId()
		{
			bool[] array = new bool[256];
			foreach (KeyValuePair<byte, LocalVoice> localVoice in localVoices)
			{
				array[localVoice.Value.id] = true;
			}
			for (byte b = idInc(voiceIdLast); b != voiceIdLast; b = idInc(b))
			{
				if (!array[b])
				{
					voiceIdLast = b;
					return b;
				}
			}
			return 0;
		}

