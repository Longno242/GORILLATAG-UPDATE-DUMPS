				if (framePool.Info != frame.Length)
				{
					framePool.Init(frame.Length);
				}
				T[] array = framePool.AcquireOrCreate();
				Buffer.BlockCopy(frame, 0, array, 0, Buffer.ByteLength(frame));
				lock (this)
				{
					frameQueue.Enqueue(array);
					syncFrameQueue();
				}
			}
		}

