		{
			UnityEngine.Debug.Log("UnityAudioOut :: OutCreate " + source.gameObject.name, source);
			source.loop = true;
			clip = AudioClip.Create("UnityAudioOut", bufferSamples, channels, frequency, stream: false);
			source.clip = clip;
		}

		public override void OutStart()
		{
			if (source.clip != null)
			{
				source.Play();
			}
		}

		public override void OutWrite(float[] data, int offsetSamples)
		{
			clip.SetData(data, offsetSamples);
		}

		public override void Stop()
		{
			base.Stop();
			source.Stop();
			if (source != null)
			{
				source.clip = null;
				UnityEngine.Object.Destroy(clip);
				clip = null;
			}
		}

		public override void ToggleAudioSource(bool toggle)
		{
			if (toggle)
			{
				source.clip = clip;
				source.Play();
			}
			else
			{
				source.Stop();
				source.clip = null;
			}
		}
