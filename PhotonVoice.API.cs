			{
				if (flushed)
				{
					watchLast = watch.ElapsedMilliseconds;
				}
				long elapsedMilliseconds = watch.ElapsedMilliseconds;
				buf[ptr] = (short)(elapsedMilliseconds - watchLast);
				info[ptr] = lost;
				watchLast = elapsedMilliseconds;
				ptr++;
				if (ptr == buf.Length)
				{
					ptr = 0;
				}
				flushed = flush;
			}
		}
