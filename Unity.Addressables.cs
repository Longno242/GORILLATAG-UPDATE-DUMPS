			{
				return ResourceManager.CreateCompletedOperation(result: false, "Bundle cache is already being cleaned.");
			}
			m_ActiveCleanBundleCacheOperation = new CleanBundleCacheOperation(this, forceSingleThreading).Start(depOp);
