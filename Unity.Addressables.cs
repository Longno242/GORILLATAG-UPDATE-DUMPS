						{
							Hash128 hash = Hash128.Parse(assetBundleRequestOptions.Hash);
							Caching.ClearOtherCachedVersions(bundleName, hash);
						}
					}
					else
					{
						flag = flag && Caching.ClearAllCachedVersions(bundleName);
					}
				}
			}
			return flag;
		}

