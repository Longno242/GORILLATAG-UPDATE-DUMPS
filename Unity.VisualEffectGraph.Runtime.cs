					num3 = Mathf.Min(num3, 1f);
				}
				state.spawnCount += num3;
				state.vfxEventAttribute.SetVector3(oldPositionAttributeId, m_OldPosition);
				state.vfxEventAttribute.SetVector3(positionAttributeId, vector);
			}
			m_OldPosition = vector;
		}

