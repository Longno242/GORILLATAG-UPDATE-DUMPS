			}
			else if (name.StartsWith("_"))
			{
				name = name.Substring(1, name.Length - 1);
			}
			if (name[0] == 'k' && name[1] >= 'A' && name[1] <= 'Z')
			{
				name = name.Substring(1, name.Length - 1);
			}
			name = Regex.Replace(name, "(\\B[A-Z]+?(?=[A-Z][^A-Z])|\\B[A-Z]+?(?=[^A-Z]))", " $1");
			name = CultureInfo.CurrentCulture.TextInfo.ToTitleCase(name);
