					return false;
				}
			}
			try
			{
				if (Path.IsPathRooted(cleanedPath))
				{
					UnityEngine.Debug.LogWarning("Path cannot be absolute");
					return false;
				}
			}
			catch (Exception arg)
			{
				UnityEngine.Debug.LogWarning(string.Format("{0}\n{1}", "Exception caught trying to create path.", arg));
				return false;
			}
			cleanedPath = cleanedPath.Trim(k_PathTrimChars);
			int num = 0;
			for (int num2 = cleanedPath.Length - 1; num2 >= 0; num2--)
			{
				if (cleanedPath[num2] == '\\' || cleanedPath[num2] == '/')
